package fr.sword.naiad.commons.core.utils;

public class StreamUtilsException extends Exception {

	private static final long serialVersionUID = 1L;

	public StreamUtilsException() {
		super();
	}

	public StreamUtilsException(String message, Throwable cause) {
		super(message, cause);
	}

	public StreamUtilsException(String message) {
		super(message);
	}

	public StreamUtilsException(Throwable cause) {
		super(cause);
	}
	
}
