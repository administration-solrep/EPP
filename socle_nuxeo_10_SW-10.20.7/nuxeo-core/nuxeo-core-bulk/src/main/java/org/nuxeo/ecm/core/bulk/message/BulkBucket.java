package org.nuxeo.ecm.core.bulk.message;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

/**
 * A message to pass a command id and a bucket of document ids
 *
 * @since 10.2
 */
public class BulkBucket implements Serializable {

    private static final long serialVersionUID = 20181021L;

    protected String commandId;

    protected List<String> ids = new ArrayList<>();

    protected BulkBucket() {
        // Empty constructor for Avro decoder
    }

    public BulkBucket(String commandId, List<String> ids) {
        this.commandId = commandId;
        this.ids = ids;
    }

    public List<String> getIds() {
        return ids;
    }

    public void setIds(List<String> ids) {
        this.ids = ids;
    }

    public String getCommandId() {
        return commandId;
    }

    public void setCommandId(String commandId) {
        this.commandId = commandId;
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

    @Override
    public boolean equals(Object o) {
        return EqualsBuilder.reflectionEquals(this, o);
    }

    @Override
    public String toString() {
        return ToStringBuilder.reflectionToString(this);
    }
}
