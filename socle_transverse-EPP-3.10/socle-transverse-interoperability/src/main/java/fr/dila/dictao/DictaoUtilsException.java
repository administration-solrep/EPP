package fr.dila.dictao;

public class DictaoUtilsException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public DictaoUtilsException() {
		super();
	}

	public DictaoUtilsException(String message, Throwable cause) {
		super(message, cause);
	}

	public DictaoUtilsException(String message) {
		super(message);
	}

	public DictaoUtilsException(Throwable cause) {
		super(cause);
	}
}
