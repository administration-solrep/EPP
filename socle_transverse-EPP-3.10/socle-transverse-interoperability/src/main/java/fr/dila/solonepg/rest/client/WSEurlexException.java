package fr.dila.solonepg.rest.client;

public class WSEurlexException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public WSEurlexException() {
		super();
	}

	public WSEurlexException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSEurlexException(String message) {
		super(message);
	}

	public WSEurlexException(Throwable cause) {
		super(cause);
	}

}