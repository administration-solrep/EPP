package fr.dila.solonepg.rest.client;

public class WSSpeCallerException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public WSSpeCallerException() {
		super();
	}

	public WSSpeCallerException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSSpeCallerException(String message) {
		super(message);
	}

	public WSSpeCallerException(Throwable cause) {
		super(cause);
	}

}
