package fr.dila.dictao.proxy;

public class DictaoServiceCallerException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public DictaoServiceCallerException() {
		super();
	}

	public DictaoServiceCallerException(String message, Throwable cause) {
		super(message, cause);
	}

	public DictaoServiceCallerException(String message) {
		super(message);
	}

	public DictaoServiceCallerException(Throwable cause) {
		super(cause);
	}

}
