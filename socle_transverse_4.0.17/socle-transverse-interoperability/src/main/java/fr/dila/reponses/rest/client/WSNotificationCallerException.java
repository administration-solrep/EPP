package fr.dila.reponses.rest.client;

public class WSNotificationCallerException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public WSNotificationCallerException() {
		super();
	}

	public WSNotificationCallerException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSNotificationCallerException(String message) {
		super(message);
	}

	public WSNotificationCallerException(Throwable cause) {
		super(cause);
	}

}
