package fr.dila.st.utils;

public class InteropUtilsException extends Exception {

	private static final long	serialVersionUID	= 1L;

	public InteropUtilsException() {
		super();
	}

	public InteropUtilsException(String message, Throwable cause) {
		super(message, cause);
	}

	public InteropUtilsException(String message) {
		super(message);
	}

	public InteropUtilsException(Throwable cause) {
		super(cause);
	}

}
